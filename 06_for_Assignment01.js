console.log(
  `-------------------------------------------------Using For Loop---------------------------------------------------`
);
console.log(
  `----------------5 to 15 incrementing by 1------------------------`
);
for (var i = 5; i <= 15; i++) {
  console.log(i);
}
console.log(
  `----------------50 to 40 decrementing by 1------------------------`
);
for (var i = 50; i >= 40; i--) {
  console.log(i);
}
console.log(`----------------15 odd Numbers------------------------`);
for (i = 1; i <= 30; i = i + 2) {
  console.log(i);
}
console.log(`----------------10 Even Number------------------------`);
for (i = 0; i <= 18; i = i + 2) {
  console.log(i);
}
console.log(`----------------Table of 5------------------------`);
for (i = 5; i <= 50; i = i + 5) {
  console.log(i);
}
console.log(`----------------Table of 10------------------------`);
for (i = 10; i <= 100; i = i + 10) {
  console.log(i);
}

console.log(
  `----------------Table of 10 in reverse order------------------------`
);
for (i = 100; i >= 10; i = i - 10) {
  console.log(i);
}