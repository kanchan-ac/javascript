console.log(
  `-------------------------------------------------Using While Loop---------------------------------------------------`
);
console.log(
  `----------------5 to 15 incrementing by 1------------------------`
);
var i = 5;
while (i <= 15) {
  console.log(i);
  i++;
}
console.log(
  `----------------50 to 40 decrementing by 1------------------------`
);
var i = 50;
while (i >= 40) {
  console.log(i);
  i--;
}
console.log(`----------------15 Odd Numbers------------------------`);
var i = 1;
while (i <= 30) {
  console.log(i);
  i = i + 2;
}
console.log(`----------------10 Even Numbers------------------------`);
i = 0;
while (i <= 18) {
  console.log(i);
  i = i + 2;
}
console.log(`----------------Table of 5------------------------`);
i = 5;
while (i <= 50) {
  console.log(i);
  i = i + 5;
}
console.log(`----------------Table of 10------------------------`);
i = 10;
while (i <= 100) {
  console.log(i);
  i = i + 10;
}
console.log(`----------------Reverse Table of 10------------------------`);
i = 100;
while (i >= 10) {
  console.log(i);
  i = i - 10;
}
